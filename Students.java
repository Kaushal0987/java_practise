public class Students {
 
 private int id;
 private String name;
 private double fee;

 public void setID(int id)
 {
  this.id = id;
 }
 public void setName(String name)
 {
  this.name = name;
 }
 public void setFee(double fee)
 {
  this.fee = fee;
 }

 public int getID()
 {
  return id;
 }
 public String getName()
 {
  return name;
 }
 public double getFee()
 {
  return fee;
 }

}
